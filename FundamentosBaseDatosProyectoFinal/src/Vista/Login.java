    private void limpiarCampos(){
        jCheckBox1.setSelected(false);
        jtxtCorreo.setText("");
        jPasswordField1.setText("");
        jPasswordField1.setEchoChar('*'); 
    }
    private boolean validarCampos() {
        if (jtxtCorreo.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Se deben llenar el campo de correo.");
            return false;
        }else if (jPasswordField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Se deben llenar en campo de contrasenia.");
            return false;
        }else if (jPasswordField1.getText().length()<8){
            JOptionPane.showMessageDialog(rootPane, "La contrasenia debe de ser almenos 8 digitos.");
            return false;
        }
        else{
            return true;
        }
    }
